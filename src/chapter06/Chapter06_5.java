	/**
	 * <h5>5.2 기본 생성자(default constructor)</h5><br>
	 * 모든 클래스에는 반드시 하나 이상의 생성자가 정의되어 있어야 한다.<br>
	 * 지금까지 클래스에 생성자를 정의하지 않고도 인스턴스를 생성할 수 있었던 이유는 컴파일러가 제공하는 '기본 생성자(default constructor)'덕분이었다.<br>
	 * 컴파일 할 때, 소스파일(*.java)의 클래스에 생성자가 하나도 정의되지 않은 경우 컴파일러는 자동적으로 아래와 같은 내용의 기본 생성자를 추가하여 컴파일 한다.<br>
	 * <pre><code>
	 *     클래스이름() {}
	 *     Card() { }
	 * </code></pre>
	 * <br>
	 * 컴파일러가 자동적으로 추가해주는 기본 생성자는 이와 같이 매개변수도 없고 아무런 내용도 없는 아주 간단한 것이다.<br>
	 * <small>※클래스의'접근 제어자(Access Modifier)'가 public인 경우에는 기본 생성자로 'public 클래스이름() {}'이 추가된다.</small><br>
	 * 컴파일러가 자동적으로 기본 생성자를 추가해주는 경우는 '클래스 내에 생성자가 하나도 없을 때'뿐이라는 것을 명심해야 한다.<br>
	 * <b>기본 생성자가 컴파일러에 의해서 추가되는 경우는 클래스에 정의된 생성자가 하나도 없을 때 뿐이다.</b>
	 */
