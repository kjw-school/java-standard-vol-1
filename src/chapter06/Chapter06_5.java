	/**
	 * <h5>5.3 매개변수가 있는 생성자</h5><br>
	 * 생성자도 메서드처럼 매개변수를 선언하여 호출 시 값을 넘겨받아서 인스턴스의 초기화 작업에 사용할 수 있다.<br>
	 * 인스턴스마다 각기 다른 값으로 초기화되어야하는 경우가 많기 때문에 매개변수를 사용한 초기화는 매우 유용하다.<br>
	 * <pre><code>
	 *     class Car {
	 *         String color; // 색상
	 *         String gearType; // 변속기 종류 - auto(자동), manual(수동)
	 *         int door; // 문의 개수
	 *         Car() {} // 생성자
	 *         Car(String c, String g, int d) { // 생성자
	 *         		color = c;
	 *         		gearType = g;
	 *         		door = d;
	 *         }
	 *     }
	 * </code></pre>
	 * <br>
	 * Car인스턴스를 생성할 때, 생성자 Car()를 사용한다면, 인스턴스를 생성한 다음에 인스턴스변수들을 따로 초기화해주어야 하지만, 매개변수가 있는 생성자 Car(String color, String gearType, int door)를 사용한다면 인스턴스를 생성하는 동시에 원하는 값으로 초기화를 할 수 있게 된다.
	 */
