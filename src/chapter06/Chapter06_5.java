	/**
	 * <h5>5.4 생성자에서 다른 생성자 호출하기 - this(), this</h5><br>
	 * <pre><code>
	 *     - 생성자의 이름으로 클래스이름 대신 this를 사용한다.
	 *     - 한 생성자에서 다른 생성자를 호출할 때는 반드시 첫 줄에서만 호출이 가능하다.
	 *
	 *     Car(String color) {
	 *         door = 5; // 첫 번째 줄
	 *         Car(color, "auto", 4); // 에러1. 생성자의 두 번쨰 줄에서 다른 생성자 호출
	 *         						  // 에러2. this(color, "auto", 4);로 해야함
	 *     }
	 * </code></pre>
	 * <br>
	 * 생성자 내에서 다른 생성자를 호출할 때는 클래스이름인 'Car'대신 'this'를 사용해야하는데 그러지 않아서 에러이고, 또 다른 에러는 생성자 호출이 첫 번째 줄이 아닌 두 번째 줄이기 때문에 에러이다.<br>
	 * 생성자에서 다른 생성자를 첫 줄에서만 호출이 가능하도록 한 이유는 생성자 내에서 초기화 작업도중에 다른 생성자를 호출하게 되면, 호출된 다른 생성자 내에서도 멤버변수들의 값을 초기화를 할 것이므로 다른 생성자를 호출하기 이전의 초기화 작업이 무의미해질 수 있기 때문이다.<br>
	 * 'this'는 참조변수로 인스턴스 자신을 가리킨다. 참조변수를 통해 인스턴스의 멤버에 접근할 수 있는 것처럼, 'this'로 인스턴스변수에 접근할 수 있는 것이다.<br>
	 * 하지만, 'this'를 사용할 수 있는 것은 인스턴스멤버뿐이다. static메서드(클래스 메서드)에서는 인스턴스 멤버들을 사용할 수 없는 것처럼, 'this' 역시 사용할 수 없다.<br>
	 * 왜냐하면, static메서드는 인스턴스를 생성하지 않고도 호출될 수 있으므로 static메서드가 호출된 시점에 인스턴스가 존재하지 않을 수도 있기 때문이다.<br>
	 * 사실 생성자를 포함한 모든 인스턴스메서드에는 자신이 관련된 인스턴스를 가리키는 참조변수'this'가 지역변수로 숨겨진 채로 존재한다.<br>
	 * 일반적으로 인스턴스메서드는 특정 인스턴스와 관련된 작업을 하기 때문에 자신과 관련된 인스턴스의 정보가 필요하지만, static메서드는 인스턴스와 관련 없는 작업을 하므로 인스턴스에 대한 정보가 필요 없기 때문이다.<br>
	 * <pre><code>
	 *     this - 인스턴스 자신을 가리키는 참조변수, 인스턴스의 주소가 저장되어 있다. 모든 인스턴스메서드에 지역변수로 숨겨진 채로 존재한다.
	 *     this(), this(매개변수) - 생성자, 같은 클래스의 다른 생성자를 호출할 때 사용한다.
	 * </code></pre>
	 * <br>
	 * <small>※this와 this()는 비슷하게 생겼을 뿐 완전히 다른 것이다. this는'참조 변수'이고, this()는 '생성자'이다.</small>
	 */
